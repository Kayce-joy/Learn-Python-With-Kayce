"""
Variables and Data Types:
In Python, variables are used to store data. You can think of a variable as a box
where you store something (like a number or a name). Data types tell Python what
kind of data you're storing.
● Integer: Whole numbers (e.g., 5)
● Float: Numbers with decimal points (e.g., 3.14)
● String: Text (e.g., "Alice")
● Boolean: True or False values (e.g., True)
x = 5 # Integer
y = 3.14 # Float
name = "Alice" # String
is_active = True # Boolean
"""

x = 5         # Integer
y = 3.14      # Float
name = "Alice"  # String
is_active = True  # Boolean

# Printing values and their types
print(x, type(x))  
print(y, type(y))  
print(name, type(name))  
print(is_active, type(is_active))  